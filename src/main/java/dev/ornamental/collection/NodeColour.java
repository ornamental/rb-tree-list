package dev.ornamental.collection;

/**
 * This class contains node colour flag aliases.
 */
public final class NodeColour {

	/**
	 * The red nodes have the colour flag set.
	 */
	public static final boolean RED = true;

	/**
	 * The black nodes have the colour flag unset.
	 */
	public static final boolean BLACK = false;

	private NodeColour() { }
}
